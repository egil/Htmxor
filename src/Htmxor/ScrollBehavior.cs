namespace Htmxor;

/// <summary>
/// The behavior for a boosted link on page transitions.
/// </summary>
public enum ScrollBehavior
{
	/// <summary>
	/// Smooth will smooth-scroll to the top of the page.
	/// </summary>
	smooth,

	/// <summary>
	/// Auto will behave like a vanilla link.
	/// </summary>
	auto,
}

